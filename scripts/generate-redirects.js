/**
 * Generate a redirect map for removed product/insurer URLs.
 * Reads /tmp/removed-products.txt and outputs a TypeScript map for use in proxy.ts.
 */
const fs = require("fs");
const path = require("path");

const REMOVED = fs.readFileSync(path.join(__dirname, "removed.txt"), "utf-8")
  .trim()
  .split("\n")
  .map((l) => l.split("|"))
  .filter(([id, cat]) => id && cat);

// Build redirect map: { productId: category }
const map = {};
for (const [id, cat] of REMOVED) {
  map[id] = cat;
}

const out = `// Auto-generated by scripts/generate-redirects.js
// Maps removed product IDs to their original categories for redirects.
// This handles 404s from URLs that Google indexed before the 2026-04-03 data rebuild.

export const REMOVED_PRODUCT_MAP: Record<string, string> = ${JSON.stringify(map, null, 2)};
`;

const target = path.join(__dirname, "..", "src/lib/removed-products.ts");
fs.writeFileSync(target, out);
console.log(`Wrote ${Object.keys(map).length} removed product mappings to ${target}`);
