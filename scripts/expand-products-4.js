const fs = require('fs');
const path = require('path');

function writeData(cc, category, newProducts) {
  const fname = path.join(__dirname, '..', 'src/data', cc, `${category}-insurance.json`);
  const existing = JSON.parse(fs.readFileSync(fname, 'utf-8'));
  const existingIds = new Set(existing.products.map(p => p.id));
  const toAdd = newProducts.filter(p => !existingIds.has(p.id));
  existing.products.push(...toAdd);
  existing.lastUpdated = '2026-04-13';
  fs.writeFileSync(fname, JSON.stringify(existing, null, 2));
  console.log(`${cc}/${category}: +${toAdd.length} (total ${existing.products.length})`);
}

